class MmD {

    constructor(mmd_id, name, age) {
        this.mmd_id = mmd_id;
        this.name = name;
        this.age = age;
    }

    setMmD_Id(mmd_id) {
        this.mmd_id = mmd_id;
    }
    getMmD_Id() {
        return this.mmd_id;
    }
    setName(name){
        this.name = name;
    }
    getName(){
        return this.name;
    }
    setAge(age){
        this.age = age;
    }
    getAge(){
        return this.age;
    }

}